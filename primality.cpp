bool check_primality(long int n);

int main()
{
    int p;
    cin >> p;
    for (int a0 = 0; a0 < p; a0++)
    {
        long int n;
        cin >> n;
        bool is_prime = check_primality(n);
        is_prime ? cout << "Prime" << endl : cout << "Not prime" << endl;
    }
    return 0;
}

bool check_primality(long int n)
{
    if (n <= 1)
        return false;
    if (n <= 3)
        return true;

    if (n % 2 == 0 || n % 3 == 0)
        return false;

    // wikipedia logic
    for (int i = 5; i * i <= n; i = i + 6)
    {
        if (n % i == 0 || n % (i + 2) == 0)
        {
            return false;
        }
    }

    return true;
}